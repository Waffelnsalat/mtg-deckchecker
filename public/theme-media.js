window.MtgDeckcheckerThemeMedia = {
  create({
    elements,
    config,
    helpers,
  }) {
    const storageKey = config.themeStorageKey ?? "mtg-deckchecker-theme";
    const ambientCardPool = MTG_DECKCHECKER_AMBIENT_CARD_POOL;
    const heroCardPool = [...ambientCardPool];
    const normalizeCardKey = helpers.normalizeCardKey;
    let ambientResizeHandle = 0;
    let themeMediaQuery = null;

    function initialize() {
      themeMediaQuery = window.matchMedia("(prefers-color-scheme: dark)");
      applyTheme(getPreferredTheme());
      themeMediaQuery.addEventListener?.("change", syncSystemThemePreference);
      elements.toggle?.addEventListener("click", () => {
        const nextTheme = getActiveTheme() === "dark" ? "light" : "dark";
        window.localStorage.setItem(storageKey, nextTheme);
        applyTheme(nextTheme);
      });
      window.addEventListener("resize", scheduleAmbientRender);
      renderHeroMediaCards();
      renderAmbientCardBackground();
    }

    function getCardByName(name) {
      const key = normalizeCardKey(name);
      if (!key) {
        return null;
      }

      return ambientCardPool.find((card) => normalizeCardKey(card.name) === key) ?? null;
    }

    function getStoredTheme() {
      const storedTheme = window.localStorage.getItem(storageKey);
      return storedTheme === "dark" || storedTheme === "light" ? storedTheme : null;
    }

    function getPreferredTheme() {
      const storedTheme = getStoredTheme();
      if (storedTheme) {
        return storedTheme;
      }

      return window.matchMedia("(prefers-color-scheme: dark)").matches ? "dark" : "light";
    }

    function getActiveTheme() {
      return document.documentElement.dataset.theme === "dark" ? "dark" : "light";
    }

    function applyTheme(theme) {
      document.documentElement.dataset.theme = theme;

      if (elements.toggle) {
        const nextModeLabel = theme === "dark" ? "Light mode" : "Dark mode";
        elements.toggle.textContent = nextModeLabel;
        elements.toggle.setAttribute("aria-pressed", String(theme === "dark"));
        elements.toggle.setAttribute(
          "aria-label",
          `Switch to ${theme === "dark" ? "light" : "dark"} mode`,
        );
      }

      if (elements.ambientBackground?.childElementCount) {
        renderAmbientCardBackground();
      }
    }

    function syncSystemThemePreference(event) {
      if (getStoredTheme() !== null) {
        return;
      }

      applyTheme(event.matches ? "dark" : "light");
    }

    function renderAmbientCardBackground() {
      if (!elements.ambientBackground) {
        return;
      }

      const slots = buildAmbientCardSlots();
      const cards = slots.map((slot, index) =>
        createAmbientCard(ambientCardPool[index % ambientCardPool.length], slot, index),
      );
      elements.ambientBackground.replaceChildren(...cards);
    }

    function buildAmbientCardSlots() {
      const preset = getAmbientCardLayoutPreset();
      const viewportWidth = Math.max(window.innerWidth || 0, document.documentElement.clientWidth || 0);
      const viewportHeight = Math.max(window.innerHeight || 0, document.documentElement.clientHeight || 0);
      const slots = [];
      let slotIndex = 0;

      for (
        let rowIndex = 0, top = preset.startY;
        top <= viewportHeight + preset.edgeY && slots.length < preset.maxSlots;
        rowIndex += 1, top += preset.gapY
      ) {
        const rowOffset = rowIndex % 2 === 0 ? 0 : preset.rowOffset;

        for (
          let left = preset.startX + rowOffset;
          left <= viewportWidth + preset.edgeX && slots.length < preset.maxSlots;
          left += preset.gapX
        ) {
          slots.push({
            left: `${left}px`,
            top: `${top}px`,
            width: preset.widths[slotIndex % preset.widths.length],
            rotation: preset.rotations[slotIndex % preset.rotations.length],
            scale: "1",
          });
          slotIndex += 1;
        }
      }

      return slots;
    }

    function getAmbientCardLayoutPreset() {
      const viewportWidth = Math.max(window.innerWidth || 0, document.documentElement.clientWidth || 0);
      const themePresets =
        getActiveTheme() === "dark"
          ? MTG_DECKCHECKER_AMBIENT_CARD_LAYOUT_PRESETS.dark
          : MTG_DECKCHECKER_AMBIENT_CARD_LAYOUT_PRESETS.light;

      if (viewportWidth <= 640) {
        return themePresets.mobile;
      }

      if (viewportWidth <= 960) {
        return themePresets.tablet;
      }

      return themePresets.desktop;
    }

    function renderHeroMediaCards() {
      if (!elements.heroImages.length) {
        return;
      }

      const cards = shuffleArray(heroCardPool).slice(0, elements.heroImages.length);
      elements.heroImages.forEach((image, index) => {
        const card = cards[index];
        if (!card) {
          return;
        }

        image.src = card.imageUrl;
        image.alt = "";
        image.title = card.name;
        image.loading = "eager";
        image.decoding = "async";
        image.referrerPolicy = "no-referrer";
      });
    }

    function createAmbientCard(card, slot, index) {
      const figure = document.createElement("figure");
      figure.className = `ambient-card ambient-card-slot-${index + 1}`;
      figure.style.left = slot.left;
      figure.style.top = slot.top;
      figure.style.width = slot.width;
      figure.style.setProperty("--ambient-rotation", slot.rotation);
      figure.style.setProperty("--ambient-scale", slot.scale);
      figure.style.setProperty("--ambient-slot", String(index));

      const image = document.createElement("img");
      image.src = card.imageUrl;
      image.alt = "";
      image.loading = "eager";
      image.decoding = "async";
      image.referrerPolicy = "no-referrer";
      image.title = card.name;

      figure.append(image);
      return figure;
    }

    function scheduleAmbientRender() {
      if (!elements.ambientBackground) {
        return;
      }

      window.clearTimeout(ambientResizeHandle);
      ambientResizeHandle = window.setTimeout(() => {
        renderAmbientCardBackground();
      }, 140);
    }

    function shuffleArray(values) {
      const clone = [...values];

      for (let index = clone.length - 1; index > 0; index -= 1) {
        const swapIndex = Math.floor(Math.random() * (index + 1));
        [clone[index], clone[swapIndex]] = [clone[swapIndex], clone[index]];
      }

      return clone;
    }

    return {
      getActiveTheme,
      getCardByName,
      initialize,
      renderAmbientCardBackground,
      renderHeroMediaCards,
    };
  },
};
